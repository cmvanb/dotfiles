#!/usr/bin/env bash
#-------------------------------------------------------------------------------
# Open with Qutebrowser
#-------------------------------------------------------------------------------

set -euo pipefail

# Imports
#-------------------------------------------------------------------------------

source "$XDG_OPT_HOME/shell-utils/debug.sh"

# Validation
#-------------------------------------------------------------------------------

debug::assert_dependency qutebrowser

# Open with Qutebrowser
#-------------------------------------------------------------------------------

qutebrowser --target window --untrusted-args "$@"
